				A recipes with cheese, meat, tomato.
				<h2>Ingredients</h2>
				<ul>
					<li>9 lasagna noodles</li>
					<li>1 pound ground beef</li>
					<li>½ onion, diced</li>
					<li>¼ cup diced green bell pepper</li>
					<li>2 cloves garlic, minced</li>
					<li>3 tablespoons butter</li>
					<li>3 tablespoons all-purpose flour</li>
					<li>1 cup milk</li>
					<li>½ cup heavy whipping cream</li>
					<li>1 ½ (8 ounce) cans tomato sauce</li>
					<li>½ cup grated Parmesan cheese</li>
					<li>salt and ground black pepper to taste</li>
					<li>1 pinch herbes de Provence</li>
					<li>garlic powder</li>
					<li>2 cups shredded Swiss cheese</li>
					<li>1 cup shredded mozzarella cheese</li>
				</ul>
				<h2>Steps</h2>
				<ol>
					<li>Bring a large pot of lightly salted water to a boil. Cook lasagna noodles in the boiling water, stirring occasionally until tender yet firm to the bite, about 8 minutes. Drain.</li>
					<li>Preheat oven to 350 degrees F (175 degrees C).</li>
					<li>Combine ground beef, onion, green bell pepper, and minced garlic in a large skillet over medium heat; cook and stir until beef is no longer pink, 5 to 8 minutes.</li>
					<li>Melt butter in a saucepan over medium-low heat. Stir in flour; cook for 1 minute. Pour in milk and heavy cream. Cook, stirring frequently, until sauce thickens, about 5 minutes.</li>
					<li>Spread 1/4 of the tomato sauce in the bottom of a 9x13-inch baking dish. Cover with 3 lasagna noodles. Spoon 1/3 of the beef mixture over the noodles. Spread 1/4 of the tomato sauce and 1/3 of the white sauce over the beef mixture. Sprinkle Parmesan cheese, salt, pepper, herbes de Provence, and garlic powder on top. Cover with 1/3 of the Swiss cheese and mozzarella cheese.i</li>
					<li>Repeat layers twice, starting with noodles and ending with cheeses.</li>
					<li>Bake lasagna in the preheated oven until top is deeply browned, 30 to 45 minutes.</li>
				</ol>
